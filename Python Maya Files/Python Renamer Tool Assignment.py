import maya.cmds as cmds

def renaming (name):

   cmds.ls(sl=True)
   
   name = "arm_##_geo"

   for i in name:

        "arm_name_geo".count("name")

        "arm_name_geo".partition("name")

        "arm_name_name_geo".partition("name")

        "arm_name_geo" + name(i).zfill(2) + "arm_name_name_geo"
        

renaming(name)































